class SingleBoomPost {
  final String imgUrl;
  final String desc;
  final String location;
  final String chain;
  final int likes;
  final int loves;
  final int smiles;
  final int rebooms;
  final int reported;
  final int comments;

  SingleBoomPost({
    required this.location,
    required this.chain,
    required this.imgUrl,
    required this.desc,
    required this.likes,
    required this.loves,
    required this.smiles,
    required this.rebooms,
    required this.reported,
    required this.comments,
  });
}
